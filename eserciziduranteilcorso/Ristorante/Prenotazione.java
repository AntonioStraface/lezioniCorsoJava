package eserciziduranteilcorso.Ristorante;

public class Prenotazione {
	private String nome;
	private int numero;
	
	public Prenotazione() {
		setNome(nome);
		setNumero(numero);
	}

	public String getNome() {
		return nome;
	}
	
	public int getNumero() {
		return numero;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}  
	
	public void setNumero(int numero) {
		this.numero = numero;
	}
	
}
